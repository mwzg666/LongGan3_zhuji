/*设置OLED的D/C# IO*/

#define Oled_DC(x)\
	    do\
	    	{\
			P4OUT &=0xFD;\
			P4OUT |=(x<<1);\
		}while(0)

/*设置OLED的CS# IO口*/

#define Oled_CS(x)\
	    do\
	    	{\
			P3OUT &=0xF7;\
			P3OUT |=(x<<3);\
		}while(0)

/*设置OLED的R/W# IO口*/

#define Oled_WR(x)\
	    do\
	    	{\
			P2OUT &=0xFE;\
			P2OUT |=x;\
		}while(0)

/*设置OLED的使能IO口*/

#define Oled_E(x)\
	    do\
	    	{\
			P2OUT &=0xFD;\
			P2OUT |=(x<<1);\
		}while(0)

/*设置OLED的数据线对应IO口*/

#define Oled_DATA(x)\
	    do\
	    	{\
	    		P2OUT &=0xF3;\
	    		P3OUT &=0xF9;\
	    		P4OUT &=0xC3;\
			P2OUT |=((x&0x04)<<1)+((x&0x08)>>1);\
			P3OUT |=((x&0x01)<<2)+(x&0x02);\
			P4OUT |=((x&0x10)>>2)+(x&0x20)+((x&0x40)>>2)+((x&0x80)>>4);\
		}while(0)



void Oled_Init()
{
	P2DIR |= BIT0+BIT1+BIT2+BIT3;
	P3DIR |= BIT1+BIT2+BIT3+BIT6;
	P4DIR |= BIT1+BIT2+BIT3+BIT4+BIT5;

	
	Oled_RST();
	Oled_InitReg();
	
}

